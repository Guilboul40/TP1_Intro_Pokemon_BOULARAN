#ifndef TD1_INTRODUCTION_C___EX1_STATE_H
#define TD1_INTRODUCTION_C___EX1_STATE_H


#include <iostream>
#include <memory>
using namespace std;

class State {
protected:
    State *currentState;
public:
    explicit State(State *state);
    virtual ~State() {}
    virtual void handle() = 0;
};


class Action {
private:
    State *currentState;
public:
    Action();
    ~Action();

    void setState(State *state) {
        delete currentState;
        currentState = state;
    }

};

#endif //TD1_INTRODUCTION_C___EX1_STATE_H